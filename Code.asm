START:
   MOV P2, #00000000B
   JB P0.0, SENSOR1A
   JMP START
SENSOR1A:
   SETB P2.0 ; SENSOR 1
   CLR P2.1 ; SENSOR 2
   SETB P2.2 ; LED
   JB P0.1, SENSOR2
   JMP SENSOR1A
SENSOR2:
   CLR P2.2
   SETB P2.0
   SETB P2.1
   JNB P0.1, SENSOR1B
   JMP SENSOR2
SENSOR1B:
   SETB P2.0
   CLR P2.1
   CLR P2.2
   JNB P0.0, START2
   JB P0.1, SENSOR2
   JMP SENSOR1B
START2:
   SETB P2.2
   CLR P2.0
   CLR P2.1
   JB P0.0, SENSOR1A
   JMP START2
END